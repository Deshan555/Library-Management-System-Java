    public void damage_books()
    {
        Random rand = new Random();

        int value = rand.nextInt(99999);

        String random_id = "DMG-"+String.valueOf(value);
        
        String SQL = "INSERT INTO Damage_Books VALUES('"+random_id+"',"+jTextField2.getText()+","+jTextField3.getText()+",'"+jTextField1.getText()+"','"+jTextField4.getText()+"','"+get_localDate.LocalDate()+"','"+jTextArea1.getText()+"','images/"+clue_path.getText()+"');";
        
        Connection conn = SQLite_Connector.connect_0();
        
        try
        {
            PreparedStatement preparedStatement = conn.prepareStatement(SQL);

            preparedStatement.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Record Saved Successfully");
            
            Clear();
        }
        catch(SQLException ERROR)
        {
            Bugs.exceptions(String.valueOf(ERROR));
            
            JOptionPane.showMessageDialog(null,"ERROR Found : \n"+ERROR);
        }
    }
    
    
    
    
    

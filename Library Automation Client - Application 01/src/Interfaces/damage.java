    public void get_find(int ID)
    {
        Connection connection =  Connector.connection();
        
        String SQL = "SELECT * FROM books WHERE BOOK_ID = "+ID+";";
        
        try
        {
            Statement stmt = connection.createStatement();
            
            ResultSet rs = stmt.executeQuery(SQL);
            
            while(rs.next())
            {
                value = rs.getString("BOOK_PRICE");
                
                System.out.println(value);
                
                Integer full_price = Integer.valueOf(value)/100*10;
                
                value = String.valueOf(full_price);
                
                jTextField4.setText("LKR "+value+".00");
            }
        }
        catch(SQLException ERROR)
        {
            Bugs.exceptions(String.valueOf(ERROR));
            
            System.out.println(ERROR);
        }
    }

    
